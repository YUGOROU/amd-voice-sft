"""
EQ-Bench v3 evaluation wrapper.

EQ-Bench v3 is judge-graded and covers three task types: role-play, message
drafting, and analysis. It uses TrueSkill-based ELO alongside rubric scoring.

This script clones the official eqbench3 repo on first run, configures it to
point at a local vLLM server for the test model, and calls the benchmark CLI.
A judge model (Anthropic or OpenAI) is required for grading.

Usage:
  # baseline
  python eval/run_eqbench3.py \
    --model Qwen/Qwen3-4B-Instruct \
    --base-url http://localhost:8000/v1 \
    --judge-model anthropic/claude-sonnet-4-6 \
    --judge-api-key $ANTHROPIC_API_KEY \
    --output eval/results/eqbench3_base.json

  # fine-tuned
  python eval/run_eqbench3.py \
    --model YUGOROU/lumi-qwen3-4b \
    --base-url http://localhost:8000/v1 \
    --judge-model anthropic/claude-sonnet-4-6 \
    --judge-api-key $ANTHROPIC_API_KEY \
    --output eval/results/eqbench3_finetuned.json

  # use OpenAI as judge instead
  python eval/run_eqbench3.py \
    --model Qwen/Qwen3-4B-Instruct \
    --base-url http://localhost:8000/v1 \
    --judge-model openai/gpt-4o-mini \
    --judge-api-key $OPENAI_API_KEY \
    --output eval/results/eqbench3_base.json

  # rubric only (skip ELO, much faster)
  python eval/run_eqbench3.py ... --no-elo

Judge model note:
  The eqbench3 benchmark requires a capable judge. Use at least claude-haiku-4-5
  or gpt-4o-mini. Weaker judges produce unreliable scores.
"""

import argparse
import json
import os
import subprocess
import sys
import textwrap
from pathlib import Path

REPO_URL = "https://github.com/EQ-bench/eqbench3.git"
REPO_DIR = Path(__file__).parent / "eqbench3"


def clone_repo() -> None:
    if REPO_DIR.exists():
        print(f"eqbench3 repo already at {REPO_DIR}, skipping clone.")
        return
    print(f"Cloning eqbench3 → {REPO_DIR} ...")
    subprocess.run(["git", "clone", "--depth", "1", REPO_URL, str(REPO_DIR)], check=True)
    print("Cloned.")


def install_deps() -> None:
    req = REPO_DIR / "requirements.txt"
    if not req.exists():
        print("Warning: no requirements.txt found in eqbench3 repo, skipping dep install.")
        return
    print("Installing eqbench3 dependencies...")
    subprocess.run(
        [sys.executable, "-m", "pip", "install", "-q", "-r", str(req)],
        check=True,
    )


def write_env(
    base_url: str,
    test_api_key: str,
    judge_api_key: str,
    judge_base_url: str,
) -> Path:
    """Write a .env file in the eqbench3 directory configured for our setup.

    eqbench3/utils/api.py reads TEST_API_KEY/TEST_API_URL for the test model
    and JUDGE_API_KEY/JUDGE_API_URL for the judge — it does NOT use LiteLLM or
    OPENAI_API_BASE. The URLs must be full chat-completions endpoints.
    """
    env_path = REPO_DIR / ".env"

    # Ensure the URL ends with /chat/completions as eqbench3 posts directly to it.
    def completions_url(base: str) -> str:
        base = base.rstrip("/")
        if not base.endswith("/chat/completions"):
            base += "/chat/completions"
        return base

    lines = [
        "# Auto-generated by eval/run_eqbench3.py — do not edit manually",
        "",
        "# ── Test model (local vLLM) ──────────────────────────────────────────",
        f"TEST_API_KEY={test_api_key}",
        f"TEST_API_URL={completions_url(base_url)}",
        "",
        "# ── Judge model (crof.ai / custom OpenAI-compatible) ─────────────────",
        f"JUDGE_API_KEY={judge_api_key}",
        f"JUDGE_API_URL={completions_url(judge_base_url)}",
        "",
        "# ── Context cap (vLLM max-model-len=4096) ────────────────────────────",
        "MAX_TOKENS_CAP=3500",
    ]

    env_path.write_text("\n".join(lines) + "\n")
    return env_path


def run_benchmark(
    test_model_arg: str,
    judge_model: str,
    threads: int,
    iterations: int,
    no_elo: bool,
    no_rubric: bool,
    extra_args: list[str],
) -> subprocess.CompletedProcess:
    cmd = [
        sys.executable, str(REPO_DIR / "eqbench3.py"),
        "--test-model", test_model_arg,
        "--judge-model", judge_model,
        "--threads", str(threads),
        "--iterations", str(iterations),
    ]
    if no_elo:
        cmd.append("--no-elo")
    if no_rubric:
        cmd.append("--no-rubric")
    cmd.extend(extra_args)

    print("\nRunning:", " ".join(cmd))
    print("-" * 60)
    result = subprocess.run(cmd, cwd=str(REPO_DIR), capture_output=False)
    return result


def collect_results(model_name: str, output_path: str) -> dict:
    """
    Try to parse whatever result files eqbench3 wrote and package them for our
    eval results directory. Falls back to a stub if parsing fails.
    """
    summary: dict = {"model": model_name, "benchmark": "eq-bench-v3"}

    # eqbench3 stores results in its own JSON files; glob for them.
    result_files = list(REPO_DIR.glob("**/*results*.json")) + list(REPO_DIR.glob("**/*runs*.json"))
    raw_results = []
    for rf in result_files:
        try:
            data = json.loads(rf.read_text())
            if isinstance(data, (dict, list)):
                raw_results.append({"file": str(rf.relative_to(REPO_DIR)), "data": data})
        except Exception:
            pass

    summary["raw_result_files"] = raw_results

    # Best-effort: look for a rubric score that resembles a top-level number.
    for entry in raw_results:
        d = entry["data"]
        if isinstance(d, dict):
            for key in ("rubric_score", "eq_bench_score", "score", "mean_score"):
                if key in d:
                    summary["eq_bench_score"] = d[key]
                    break

    os.makedirs(os.path.dirname(output_path) or ".", exist_ok=True)
    with open(output_path, "w") as f:
        json.dump(summary, f, indent=2)
    print(f"\nSaved summary → {output_path}")
    return summary


def main() -> None:
    parser = argparse.ArgumentParser(
        formatter_class=argparse.RawDescriptionHelpFormatter,
        description=textwrap.dedent(__doc__),
    )
    parser.add_argument("--model", required=True,
                        help="Model name as served by vLLM (e.g. Qwen/Qwen3-4B-Instruct)")
    parser.add_argument("--base-url", default=os.getenv("VLLM_BASE_URL", "http://localhost:8000/v1"),
                        help="vLLM OpenAI-compatible base URL")
    parser.add_argument("--test-api-key", default="not-required",
                        help="API key for the test model endpoint (vLLM ignores this)")
    parser.add_argument("--judge-model", default=None,
                        help="LiteLLM-style judge model (default: same model as test, i.e. self-judge via vLLM)")
    parser.add_argument("--judge-api-key", default=os.getenv("ANTHROPIC_API_KEY") or os.getenv("OPENAI_API_KEY") or "not-required",
                        help="API key for the judge model provider (not needed when self-judging via vLLM)")
    parser.add_argument("--judge-base-url", default="https://crof.ai/v1",
                        help="Base URL for the judge model's OpenAI-compatible endpoint (default: https://crof.ai/v1)")
    parser.add_argument("--threads", type=int, default=4,
                        help="Parallel API threads (default: 4)")
    parser.add_argument("--iterations", type=int, default=1,
                        help="Number of times to repeat each scenario (default: 1)")
    parser.add_argument("--no-elo", action="store_true",
                        help="Skip TrueSkill ELO ranking (faster, rubric-only)")
    parser.add_argument("--no-rubric", action="store_true",
                        help="Skip rubric scoring (ELO only)")
    parser.add_argument("--skip-install", action="store_true",
                        help="Skip pip install of eqbench3 requirements")
    parser.add_argument("--output", default="eval/results/eqbench3.json",
                        help="Where to write the summary results JSON")
    args, extra = parser.parse_known_args()

    # 1. Setup
    clone_repo()
    if not args.skip_install:
        install_deps()

    # eqbench3 passes the model string directly in the API payload, so no
    # provider prefix — just the model name as served by vLLM.
    test_model_arg = args.model

    # Self-judge: if no judge model given, use the same vLLM endpoint.
    if args.judge_model is None:
        judge_model = args.model
        judge_base_url = args.base_url
        print("No judge model specified — using self-judge (same model via vLLM).")
    else:
        judge_model = args.judge_model
        judge_base_url = args.judge_base_url

    # 2. Configure .env with the env vars eqbench3 actually reads
    env_path = write_env(
        base_url=args.base_url,
        test_api_key=args.test_api_key,
        judge_api_key=args.judge_api_key,
        judge_base_url=judge_base_url,
    )
    print(f"Wrote env config → {env_path}")

    # 3. Print config summary
    print(f"\nTest model arg : {test_model_arg}")
    print(f"Judge model    : {judge_model}")
    print(f"vLLM base URL  : {args.base_url}")
    print(f"Judge base URL : {judge_base_url}")
    print(f"Threads        : {args.threads}  |  Iterations: {args.iterations}")
    if args.no_elo:
        print("ELO            : disabled")
    print()

    # 4. Run
    proc = run_benchmark(
        test_model_arg=test_model_arg,
        judge_model=judge_model,
        threads=args.threads,
        iterations=args.iterations,
        no_elo=args.no_elo,
        no_rubric=args.no_rubric,
        extra_args=extra,
    )

    # 5. Collect & save
    summary = collect_results(model_name=args.model, output_path=args.output)

    if "eq_bench_score" in summary:
        print(f"\nEQ-Bench v3 score : {summary['eq_bench_score']}")
    else:
        print("\nNote: could not auto-parse a top-level score from eqbench3 output.")
        print(f"      Check the raw result files captured in {args.output}")

    sys.exit(proc.returncode)


if __name__ == "__main__":
    main()
